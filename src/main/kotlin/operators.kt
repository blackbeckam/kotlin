fun main(){
    //arithmetic operators
    var num1:Int=34
    var num2:Int=15
    var sum=num1+num2
    println(sum)
    println(num1*num2)
    println(num1-num2)
    println(num1/num2)
    println(num1%num2)

    //Assignment operators
    var number=5
    println(number)

    var x=6
    x+=5
    println(x)

    var y=20
    y-=11

    //comparison operators
    var firstnumber=89
    var secondnumber=64
    println(firstnumber<secondnumber)
    println(firstnumber>secondnumber)
    println(firstnumber==secondnumber)
    println(firstnumber<=secondnumber)
    println(firstnumber>=secondnumber)
    println(firstnumber!=secondnumber)

    //logical operators
    var log=78
    println(log<56 && log>23)
    println(log<56 || log>23)  //or
    println(!(log<56 || log>23))

}